//Создать две переменных, одна равна 7, другая 20. Если результат перемножения этих переменных больше 10, то вывести в консоль первую переменную, в противном случае вывести вторую.

var number1: Int = 7
var number2: Int = 20
if number1 * number2 > 10{
    print(number1)
}
else{
    print(number2)
}
